public class do_something{
    public static void main(String[] args){

        // integer datatype 
        int x = 3; 

        // string datatype 
        String name = "Thomaskutty";
        
        // variable reassignment 
        x = x*10 ; 

        // printing 
        System.out.println("x is = " + x); 
        
        // method call : double datatype 
        double d = Math.random(); 

        // print formatting : string concatenation 
        System.out.println("name = " + name);
        System.out.println("random number = "+d); 
    }
}
